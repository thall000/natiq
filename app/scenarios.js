export function getCategoryById(id) {
  return categories.find((c) => c.id === id);
}

export function getScenariosByCategory(categoryId) {
  return scenarios.filter((s) => s.category === categoryId);
}

export function getScenarioById(id) {
  return scenarios.find((s) => s.id === id);
}

export function getRandomScenariosByCategory(categoryId, count = 10) {
  const pool = getScenariosByCategory(categoryId).filter((s) => s.kind !== "Rollenspiel");
  for (let i = pool.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [pool[i], pool[j]] = [pool[j], pool[i]];
  }
  return pool.slice(0, count);
}

export function getRandomRoleplayByCategory(categoryId) {
  const roleplays = getScenariosByCategory(categoryId).filter((s) => s.kind === "Rollenspiel");
  if (roleplays.length === 0) return null;
  return roleplays[Math.floor(Math.random() * roleplays.length)];
}
